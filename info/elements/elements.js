function viewElementBetastar() {
document.getElementById("#elementRarity").innerText = "Common";
document.getElementById("#elementRarity").style.color = "#FFFFFF";
document.getElementById("#elementRarity").style.textShadow = "0px 0px 25px #FFFFFF";
document.getElementById("#elementImage").src = "https://betastar.org/image/elements/blax.png";
document.getElementById("#elementName").innerText = "Betastar";
document.getElementById("#elementPrice").innerText = "0 atoms";
}

function viewElement(name) {
    var elem = elements[name];
    document.getElementById("#elementRarity").textContent = elem[0];
    var color = elem[0];
    switch (color) {
        case "Common":
            color = "FFFFFF";
            break;
        case "Uncommon":
            color = "#4bc22e";
            break;
        case "Rare":
            color = "#0a14fa";
            break;
        case "Epic":
            color = "#be0000";
            break;
        case "Legendary":
            color = "#ff910f";
            break;
        case "Fabled":
            color = "#0c7500";
            break;
        case "Perfect":
            color = "#fffacd";
            break;
        case "Divine":
            color = "#ee82ee";
            break;
    }
    document.getElementById("#elementRarity").style.color = color;
    document.getElementById("#elementRarity").style.textShadow = "0px 0px 25px " + color;
    try {
        if (typeof elem[2] != 'undefined') {
                document.getElementById("#elementImage").src = "https://betastar.org/image/elements/"+elem[2]+".png";
        } else {
            document.getElementById("#elementImage").src = "https://betastar.org/image/elements/"+(name.charAt(0).toLowerCase() + name.slice(1)).replace(' ', '')+".png";
        }
    } catch (e) {
        document.getElementById("#elementImage").src = "https://betastar.org/image/elements/"+(name.charAt(0).toLowerCase() + name.slice(1)).replace(' ', '')+".png";
    }
    document.getElementById("#elementName").textContent = name;
    document.getElementById("#elementPrice").textContent = elem[1];
}

Object.entries(elements).forEach(function (item) {
    var key = item[0].toString();
    var elem = document.createElement('img');
    
    elem.classList.add('bottomElement');
    
    try {
        if (typeof item[1][2] != 'undefined') {
            elem.src = "https://betastar.org/image/elements/"+item[1][2]+".png";
        } else {
            elem.src = "https://betastar.org/image/elements/"+(key.charAt(0).toLowerCase() + key.slice(1)).replace(' ', '')+".png";
        }
    } catch (e) {
        elem.src = "https://betastar.org/image/elements/"+(key.charAt(0).toLowerCase() + key.slice(1)).replace(' ', '')+".png";
    }
    
    elem.onclick = function () {
        viewElement(key);
        window.currentQuantity = -1;
    };
    
    document.getElementById('#elementList').appendChild(elem);
});
